package class3;

public class E11ShortHandOperator {
    public static void main(String[] args) {

        int number=100;
        number++; // increase the value by one
        System.out.println(number);

        double num=15.5;
        num++;
        System.out.println(num);

        double f=11;
        f/=2;
        System.out.println(f);

        double i=11;
        i%=4;
        System.out.println(i);

        double v=0;
        v--;
        System.out.println(v);
    }
}
