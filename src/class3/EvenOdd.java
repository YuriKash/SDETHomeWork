package class3;

public class EvenOdd {

    /*
    Write a Java Program to check whether number is Even or Odd
     */
    public static void main(String[] args) {

        int num=4;

        if(num%2==0){
            System.out.println("Even");
        }
        else{
            System.out.println("Odd");
        }
    }
}
